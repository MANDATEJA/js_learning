// !== - Not equal operator
// < - less than operator
// > - greater than operator
// <= - less than equal operator
// >= - greator than equal operator

let temp = 180

if (temp <= 32) {
    console.log('It is freezing outside!')
}

if (temp >= 110) {
    console.log('It is way too hot outside!')
}

let age = 75

if (age <= 7) {
    console.log('You will get a child discount of 10%')
}

if (age >= 65) {
    console.log('You will get senior citizen discount of 20%')
}